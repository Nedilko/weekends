import { getDay, getHour } from '@utils/convertTime'

describe('convertTime', () => {
  it('getHour should returnvalue on given day parameter', () => {
    expect(getDay('Monday')).toBe(1)
    expect(getDay('Tuesday')).toBe(2)
    expect(getDay('Wednesday')).toBe(3)
    expect(getDay('Thursday')).toBe(4)
    expect(getDay('Friday')).toBe(5)
    expect(getDay('Saturday')).toBe(6)
    expect(getDay('Sunday')).toBe(7)
  })

  it('getDay should returnvalue on given hour parameter', () => {
    expect(getHour('00:00')).toBe(0)
    expect(getHour('01:00')).toBe(1)
    expect(getHour('02:00')).toBe(2)
    expect(getHour('03:00')).toBe(3)
    expect(getHour('04:00')).toBe(4)
    expect(getHour('05:00')).toBe(5)
    expect(getHour('06:00')).toBe(6)
    expect(getHour('07:00')).toBe(7)
    expect(getHour('08:00')).toBe(8)
    expect(getHour('09:00')).toBe(9)
    expect(getHour('10:00')).toBe(10)
    expect(getHour('11:00')).toBe(11)
    expect(getHour('12:00')).toBe(12)
    expect(getHour('13:00')).toBe(13)
    expect(getHour('14:00')).toBe(14)
    expect(getHour('15:00')).toBe(15)
    expect(getHour('16:00')).toBe(16)
    expect(getHour('17:00')).toBe(17)
    expect(getHour('18:00')).toBe(18)
    expect(getHour('19:00')).toBe(19)
    expect(getHour('20:00')).toBe(20)
    expect(getHour('21:00')).toBe(21)
    expect(getHour('22:00')).toBe(22)
    expect(getHour('23:00')).toBe(23)
  })
})
